#include<stdio.h>
int main()
{
    int Num;

	printf("Enter the Number : \n");
	scanf("%d",&Num);

	printf("The Octal conversion of the number is : %o \n", Num);
	printf("The Hex conversion of the number is : %x \n", Num);
	printf("The Decimal conversion of the number is : %d \n", Num);
    printf("The Character conversion of the number is : %c \n", Num);

	return 0;

}
